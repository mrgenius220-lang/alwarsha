const CONFIG = {
  phone: "0505657130",
  whatsapp: "0505657130",
  countryCode: "966",
  maps: "https://maps.app.goo.gl/NZAAMiiJL2H9MdQr7?g_st=aw"
};

function normalizeDigits(v){
  return (v || "").toString().replace(/[^\d]/g, "");
}

function buildWhatsAppLink(text){
  const raw = normalizeDigits(CONFIG.whatsapp);
  const full = raw.startsWith("0") ? (CONFIG.countryCode + raw.slice(1)) : raw;
  const msg = encodeURIComponent(text || "");
  return msg ? `https://wa.me/${full}?text=${msg}` : `https://wa.me/${full}`;
}

function setHref(id, href){
  const el = document.getElementById(id);
  if (el) el.href = href;
}

function setText(id, text){
  const el = document.getElementById(id);
  if (el) el.textContent = text;
}

function applyI18n(langKey){
  const pack = SITE[langKey];
  document.documentElement.lang = pack.lang;
  document.documentElement.dir = pack.dir;

  const langBtn = document.getElementById("langBtn");
  if (langBtn) langBtn.textContent = `🌐 ${pack.label}`;

  const nodes = document.querySelectorAll("[data-i18n]");
  nodes.forEach(n => {
    const key = n.getAttribute("data-i18n");
    const val = pack.strings[key];
    if (typeof val === "string") n.textContent = val;
  });

  const nav = document.getElementById("nav");
  if (nav && window.innerWidth <= 720) nav.style.display = "none";

  localStorage.setItem("site_lang", langKey);
}

function initLinks(){
  const tel = `tel:${normalizeDigits(CONFIG.phone)}`;
  const map = CONFIG.maps;

  setText("phoneText", CONFIG.phone);
  setText("waText", CONFIG.whatsapp);

  setHref("callBtnTop", tel);
  setHref("callBtnAbout", tel);
  setHref("callBtnServices", tel);
  setHref("callBtnWork", tel);
  setHref("callBtnContact", tel);

  setHref("mapBtnTop", map);
  setHref("mapBtnContact", map);

  const defaultMsgAr = "السلام عليكم، عندي استفسار عن صيانة طرمبات/بخاخات ديزل. النوع: ____ ، المشكلة: ____ ، الموقع: ____";
  const defaultMsgEn = "Hello, I need help with diesel pumps/injectors. Type: ____ , Issue: ____ , Location: ____";

  const currentLang = localStorage.getItem("site_lang") || "ar";
  const msg = currentLang === "en" ? defaultMsgEn : defaultMsgAr;
  const wa = buildWhatsAppLink(msg);

  setHref("waBtnTop", wa);
  setHref("waBtnAbout", wa);
  setHref("waBtnServices", wa);
  setHref("waBtnWork", wa);
  setHref("waBtnContact", wa);
}

function initMenu(){
  const btn = document.getElementById("menuBtn");
  const nav = document.getElementById("nav");
  if (!btn || !nav) return;

  btn.addEventListener("click", () => {
    const open = nav.style.display === "flex";
    nav.style.display = open ? "none" : "flex";
  });

  document.addEventListener("click", (e) => {
    if (window.innerWidth > 720) return;
    const inside = nav.contains(e.target) || btn.contains(e.target);
    if (!inside) nav.style.display = "none";
  });

  nav.querySelectorAll("a").forEach(a => {
    a.addEventListener("click", () => {
      if (window.innerWidth <= 720) nav.style.display = "none";
    });
  });
}

function initToTop(){
  const btn = document.getElementById("toTop");
  if (!btn) return;

  const toggle = () => {
    btn.style.display = window.scrollY > 500 ? "grid" : "none";
  };

  window.addEventListener("scroll", toggle, { passive: true });
  toggle();

  btn.addEventListener("click", () => {
    window.scrollTo({ top: 0, behavior: "smooth" });
  });
}

function initForm(){
  const form = document.getElementById("contactForm");
  const note = document.getElementById("formNote");
  if (!form) return;

  form.addEventListener("submit", (e) => {
    e.preventDefault();

    const data = new FormData(form);
    const name = (data.get("name") || "").toString().trim();
    const phone = (data.get("phone") || "").toString().trim();
    const service = (data.get("service") || "").toString().trim();
    const msg = (data.get("message") || "").toString().trim();

    const currentLang = localStorage.getItem("site_lang") || "ar";
    const header = currentLang === "en" ? "New inquiry" : "طلب جديد";
    const labelName = currentLang === "en" ? "Name" : "الاسم";
    const labelPhone = currentLang === "en" ? "Phone" : "الجوال";
    const labelService = currentLang === "en" ? "Service" : "الخدمة";
    const labelMsg = currentLang === "en" ? "Message" : "الرسالة";

    const fullMsg =
      `${header}%0A` +
      `${labelName}: ${encodeURIComponent(name)}%0A` +
      `${labelPhone}: ${encodeURIComponent(phone)}%0A` +
      `${labelService}: ${encodeURIComponent(service)}%0A` +
      `${labelMsg}: ${encodeURIComponent(msg)}`;

    const wa = buildWhatsAppLink(decodeURIComponent(fullMsg));

    if (note) {
      note.textContent = currentLang === "en" ? "Opening WhatsApp..." : "جاري فتح الواتساب...";
    }

    window.open(wa, "_blank", "noopener");
    form.reset();

    setTimeout(() => {
      if (note) note.textContent = "";
    }, 2500);
  });
}

function initYear(){
  const y = document.getElementById("year");
  if (y) y.textContent = String(new Date().getFullYear());
}

function initLang(){
  const btn = document.getElementById("langBtn");
  const saved = localStorage.getItem("site_lang") || "ar";
  applyI18n(saved);
  initLinks();

  if (btn) {
    btn.addEventListener("click", () => {
      const current = localStorage.getItem("site_lang") || "ar";
      const next = current === "ar" ? "en" : "ar";
      applyI18n(next);
      initLinks();
    });
  }
}

document.addEventListener("DOMContentLoaded", () => {
  initYear();
  initLang();
  initMenu();
  initToTop();
  initForm();
});